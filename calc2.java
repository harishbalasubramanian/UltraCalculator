
public class calc2 {
	public double add(double a, double b){
		double c = a + b;
		return c;
	}
	public double subtract(double d, double e){
		double f = d-e;
		return f;
	}
	public double multiply(double g, double h) {
		double i = g*h;
		return i;
	}
	public double divide(double j, double k) {
		double l = j/k;
		return l;
	}
	public double modulo(double m, double n) {
		double o = m%n;
		return o;
	}
	public double exponent(double p, double q) {
		double r = Math.pow(p, q);
		return r;
	}
	public double square_root(double s) {
		double u = Math.sqrt(s);
		return u;
	}
	public double absolute(double t){
		double v = Math.abs(t);
		return v;
	}
	public double sin(double w){
		double x = Math.sin(w);
		return x;
	}
	public double cos(double y){
		double z = Math.cos(y);
		return z;
	}
	public double tan(double a){
		double b = Math.tan(a);
		return b;
	}
	public double asin(double a){
		double b = Math.asin(a);
		return b;
	}
	public double acos(double a){
		double b = Math.acos(a);
		return b;
	}
	public double atan(double a){
		double b = Math.atan(a);
		return b;
	}
	public double sinh(double a){
		double b = Math.sinh(a);
		return b;
	}
	public double cosh(double a){
		double b = Math.cosh(a);
		return b;
	}
	public double tanh(double a) {
		double b = Math.tanh(a);
		return b;
	}
	public double natlog(double a){
		double b = Math.log(a);
		return b;
	}
	public double log_ten(double a){
		double b = Math.log10(a);
		return b;
	}
	public double deg_rad(double a){
		double b = Math.toRadians(a);
		return b;
	}
	public double rad_deg(double a){
		double b = Math.toDegrees(a);
		return b;
	}
}
